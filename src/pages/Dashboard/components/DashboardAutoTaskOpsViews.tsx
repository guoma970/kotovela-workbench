import type { ReactNode } from 'react'
import {
  ArchiveCenterCard,
  PublishCenterCard,
} from './DashboardAutoTaskCenterCards'
import type {
  ArchiveCenterEntry,
  AutoTaskBoardItem,
  ArchiveStructureGroup,
  ConsultantSummaryEntry,
  DomainLoadSummaryEntry,
  ExecutionStatusSummary,
  ParentTaskView,
  PoolTab,
  PublishCenterEntry,
  ReassignmentRecord,
  RecentDecision,
  RecentResult,
  RiskSummary,
  TaskCardTone,
  TaskGroupView,
  TemplatePoolEntry,
} from '../lib/autoTaskShared'
import {
  formatAccountLine,
  formatContentLine,
  formatDecisionAction,
  formatPoolHealth,
  formatPoolKey,
  formatScenarioTemplate,
  formatTaskTone,
} from '../lib/autoTaskLabels'
import { formatReadableDetail, formatReadableOwner, formatReadableTaskTitle, formatReadableTime } from '../../../lib/readableText'

type TaskCardRenderer = (item: AutoTaskBoardItem, tone: TaskCardTone, index: number) => ReactNode

export function AutoTaskOperationsView({
  parentTaskViews,
  taskGroups,
  todayAutoGeneratedCount,
  riskSummary,
  loading,
  domainLoadSummary,
  humanPendingTasks,
  consultantSummary,
  reassignmentRecords,
}: {
  parentTaskViews: ParentTaskView[]
  taskGroups: TaskGroupView[]
  todayAutoGeneratedCount: number
  riskSummary: RiskSummary
  loading: boolean
  domainLoadSummary: DomainLoadSummaryEntry[]
  humanPendingTasks: AutoTaskBoardItem[]
  consultantSummary: ConsultantSummaryEntry[]
  reassignmentRecords: ReassignmentRecord[]
}) {
  return (
    <>
      <section className="scheduler-overview-card">
        <div className="scheduler-section-title">主任务 / 场景任务组进度</div>
        {parentTaskViews.length ? (
          <div className="scheduler-parent-task-list">
            {parentTaskViews.slice(0, 6).map((parent) => (
              <article className="scheduler-parent-task-card" key={parent.id}>
                <div className="scheduler-parent-task-top">
                  <strong>{formatReadableTaskTitle(parent.title)}</strong>
                  <span>{formatScenarioTemplate(parent.template)}</span>
                </div>
                <div className="scheduler-parent-task-meta">
                  <span>子任务 {parent.childCount}</span>
                  <span>进度 {parent.progress}%</span>
                  <span>阻塞点 {formatReadableTaskTitle(parent.blockedPoint)}</span>
                </div>
                <div className="scheduler-parent-task-domains">
                  {parent.domains.map((domain, domainIndex) => (
                    <span key={`${parent.id}-${domain}-${domainIndex}`} className="scheduler-parent-domain-chip">{formatReadableDetail(domain)}</span>
                  ))}
                </div>
              </article>
            ))}
          </div>
        ) : <div className="auto-task-empty">暂无主任务进度</div>}
        {taskGroups.length ? (
          <div className="scheduler-task-groups">
            {taskGroups.slice(0, 6).map((group) => (
              <div className="scheduler-task-group-chip" key={group.id}>
                <strong>{formatReadableTaskTitle(group.label)}</strong>
                <span>{formatScenarioTemplate(group.template)} · {group.count} 项任务 · {formatReadableDetail(group.domain)} / {formatReadableDetail(group.projectLine)}</span>
              </div>
            ))}
          </div>
        ) : null}
      </section>

      <section className="scheduler-overview-card">
        <div className="scheduler-section-title">运营摘要</div>
        <div className="scheduler-overview-grid scheduler-overview-grid-ops">
          <div className="scheduler-overview-metric"><span>今日自动生成</span><strong>{todayAutoGeneratedCount}</strong></div>
          <div className="scheduler-overview-metric is-warning"><span>高风险提醒</span><strong>{riskSummary.predictedHigh}</strong></div>
          <div className="scheduler-overview-metric is-warning"><span>待人工处理</span><strong>{riskSummary.needHuman}</strong></div>
          <div className="scheduler-overview-metric is-failed"><span>有卡点</span><strong>{riskSummary.blocked}</strong></div>
          <div className="scheduler-overview-metric is-warning"><span>外部合作受限</span><strong>{riskSummary.externalBlocked}</strong></div>
          <div className="scheduler-overview-metric"><span>自动转派线索</span><strong>{riskSummary.leadTransferred}</strong></div>
          <div className="scheduler-overview-metric is-concurrency"><span>同步状态</span><strong>{loading ? '刷新中' : '已同步'}</strong></div>
        </div>
        <div className="scheduler-summary-grid">
          <div className="scheduler-decisions-card">
            <div className="scheduler-section-title">各领域负载摘要</div>
            <div className="scheduler-decision-list">
              {domainLoadSummary.length ? domainLoadSummary.map((entry, index) => (
                <article className="scheduler-decision-item" key={`${entry.domain}-${index}`}>
                  <div className="scheduler-decision-top"><strong>{formatReadableDetail(entry.domain)}</strong><span>{entry.total} 项任务</span></div>
                  <p>执行中 {entry.running} · 待调度 {entry.queued}</p>
                  <small>有卡点 {entry.blocked} · 待人工处理 {entry.needHuman}</small>
                </article>
              )) : <div className="auto-task-empty">暂无领域负载数据</div>}
            </div>
          </div>
          <section className="scheduler-decisions-card scheduler-pending-human-card">
            <div className="scheduler-section-title">待人工处理摘要</div>
            <div className="scheduler-decision-list">
              {humanPendingTasks.length ? humanPendingTasks.slice(0, 8).map((item, index) => {
                const latestDecision = [...(item.decision_log ?? [])].slice(-1)[0]
                return (
                  <article className="scheduler-decision-item scheduler-human-item" key={`${item.task_name}-ops-human-${index}`}>
                    <div className="scheduler-decision-top"><strong>{formatReadableTaskTitle(item.task_name)}</strong><span>{formatReadableDetail(item.domain)}</span></div>
                    <p>{formatReadableDetail(latestDecision?.reason, '待人工处理')}</p>
                    <small>{formatReadableOwner(item.human_owner)} · {formatReadableDetail(latestDecision?.detail ?? item.manual_decision)}</small>
                  </article>
                )
              }) : <div className="auto-task-empty">暂无待人工处理任务</div>}
            </div>
          </section>
        </div>
        <div className="scheduler-summary-grid">
          <section className="scheduler-decisions-card">
            <div className="scheduler-section-title">顾问负载 / 转化率</div>
            <div className="scheduler-decision-list">
              {consultantSummary.length ? consultantSummary.map((entry, index) => (
                <article className="scheduler-decision-item" key={`${entry.consultantId}-${index}`}>
                  <div className="scheduler-decision-top"><strong>{formatReadableOwner(entry.owner)}</strong><span>{formatReadableDetail(entry.consultantId)}</span></div>
                  <p>当前工作量 {entry.activeLoad} · 总线索 {entry.total}</p>
                  <small>转化率 {entry.conversionRate}%</small>
                </article>
              )) : <div className="auto-task-empty">暂无顾问负载数据</div>}
            </div>
          </section>
          <section className="scheduler-decisions-card">
            <div className="scheduler-section-title">改派记录</div>
            <div className="scheduler-decision-list">
              {reassignmentRecords.length ? reassignmentRecords.map((item, index) => (
                <article className="scheduler-decision-item" key={`${item.lead_id}-${item.reassigned_at}-${index}`}>
                  <div className="scheduler-decision-top"><strong>{formatReadableTaskTitle(item.task_name)}</strong><span>{formatReadableTime(item.reassigned_at)}</span></div>
                  <p>{formatReadableOwner(item.consultant_id)} → {formatReadableOwner(item.reassigned_to)}</p>
                  <small>{formatReadableDetail(item.reassigned_reason)}</small>
                </article>
              )) : <div className="auto-task-empty">暂无改派记录</div>}
            </div>
          </section>
        </div>
      </section>
    </>
  )
}

export function AutoTaskExecutionView({
  poolTabs,
  normalizedActivePool,
  onSelectPool,
  executionStatusSummary,
  runningTasks,
  queuedTasks,
  blockedPoolTasks,
  needHumanPoolTasks,
  renderTaskCard,
  recentDecisions,
  recentResults,
  publishSourceGroups,
  templatePool,
  controlLoadingTask,
  onManualControlTask,
  archiveContentLineGroups,
  archiveAccountLineGroups,
  archiveStructureGroups,
  archiveDomainGroups,
  onMarkTemplateSource,
}: {
  poolTabs: PoolTab[]
  normalizedActivePool: PoolTab['key']
  onSelectPool: (poolKey: PoolTab['key']) => void
  executionStatusSummary: ExecutionStatusSummary
  runningTasks: AutoTaskBoardItem[]
  queuedTasks: AutoTaskBoardItem[]
  blockedPoolTasks: AutoTaskBoardItem[]
  needHumanPoolTasks: AutoTaskBoardItem[]
  renderTaskCard: TaskCardRenderer
  recentDecisions: RecentDecision[]
  recentResults: RecentResult[]
  publishSourceGroups: Array<[string, PublishCenterEntry[]]>
  templatePool: TemplatePoolEntry[]
  controlLoadingTask: string
  onManualControlTask: (taskName: string, action: 'mark_manual_published') => void
  archiveContentLineGroups: Array<[string, ArchiveCenterEntry[]]>
  archiveAccountLineGroups: Array<[string, ArchiveCenterEntry[]]>
  archiveStructureGroups: ArchiveStructureGroup[]
  archiveDomainGroups: Array<[string, ArchiveCenterEntry[]]>
  onMarkTemplateSource: (taskName: string) => void
}) {
  return (
    <>
      <section className="scheduler-overview-card">
        <div className="scheduler-section-title">当前协作池任务</div>
        <div className="scheduler-pool-overview">
          {poolTabs.map((pool) => (
            <button
              key={pool.key}
              type="button"
              className={`scheduler-pool-card ${normalizedActivePool === pool.key ? 'is-active' : ''} is-${pool.health}`}
              onClick={() => onSelectPool(pool.key)}
            >
              <div className="scheduler-pool-card-top"><strong>{formatPoolKey(pool.key)}</strong><span>{formatPoolHealth(pool.health)}</span></div>
              <div className="scheduler-pool-card-metrics"><span>并发 {pool.running_count}/{pool.max_concurrency}</span><span>待排队 {pool.queue_count}</span></div>
            </button>
          ))}
        </div>
        <div className="scheduler-overview-grid">
          <div className="scheduler-overview-metric is-concurrency"><span>执行中</span><strong>{executionStatusSummary.running}</strong></div>
          <div className="scheduler-overview-metric"><span>待调度</span><strong>{executionStatusSummary.queued}</strong></div>
          <div className="scheduler-overview-metric is-failed"><span>有卡点</span><strong>{executionStatusSummary.blocked}</strong></div>
          <div className="scheduler-overview-metric is-warning"><span>待人工处理</span><strong>{executionStatusSummary.needHuman}</strong></div>
          <div className="scheduler-overview-metric"><span>当前协作池</span><strong>{formatPoolKey(normalizedActivePool)}</strong></div>
        </div>
      </section>

      <section className="scheduler-queue-card">
        <div className="scheduler-section-title">调度队列 · {formatPoolKey(normalizedActivePool)}</div>
        <div className="scheduler-queue-grid">
          <div className="scheduler-lane">
            <div className="scheduler-lane-head"><h4>{formatTaskTone('running')}</h4><span>{runningTasks.length}</span></div>
            <div className="scheduler-lane-list">{runningTasks.length ? runningTasks.map((item, index) => renderTaskCard(item, 'running', index)) : <div className="auto-task-empty">暂无执行中任务</div>}</div>
          </div>
          <div className="scheduler-lane">
            <div className="scheduler-lane-head"><h4>{formatTaskTone('queue')}</h4><span>{queuedTasks.length}</span></div>
            <div className="scheduler-lane-list">{queuedTasks.length ? queuedTasks.map((item, index) => renderTaskCard(item, 'queue', index)) : <div className="auto-task-empty">暂无待调度任务</div>}</div>
          </div>
          <div className="scheduler-lane">
            <div className="scheduler-lane-head"><h4>有卡点</h4><span>{executionStatusSummary.blocked}</span></div>
            <div className="scheduler-lane-list">{blockedPoolTasks.length ? blockedPoolTasks.map((item, index) => renderTaskCard(item, 'paused', index)) : <div className="auto-task-empty">暂无有卡点任务</div>}</div>
          </div>
          <div className="scheduler-lane">
            <div className="scheduler-lane-head"><h4>待人工处理</h4><span>{executionStatusSummary.needHuman}</span></div>
            <div className="scheduler-lane-list">{needHumanPoolTasks.length ? needHumanPoolTasks.map((item, index) => renderTaskCard(item, 'done', index)) : <div className="auto-task-empty">暂无待人工处理任务</div>}</div>
          </div>
        </div>
      </section>

      <div className="scheduler-summary-grid">
        <section className="scheduler-decisions-card">
          <div className="scheduler-section-title">最近决策</div>
          <div className="scheduler-decision-list">
            {recentDecisions.length ? recentDecisions.map((decision, index) => (
              <article className="scheduler-decision-item" key={`${decision.taskName}-${decision.timestamp}-${index}`}>
                <div className="scheduler-decision-top"><strong>{formatDecisionAction(decision.decision)}</strong><span>{formatReadableTime(decision.timestamp)}</span></div>
                <p>{formatReadableTaskTitle(decision.taskName)} · {formatReadableOwner(decision.agent)}</p>
                <small>{formatReadableDetail(decision.reason)} · {formatReadableDetail(decision.detail)}</small>
              </article>
            )) : <div className="auto-task-empty">暂无自动决策记录</div>}
          </div>
        </section>
        <section className="scheduler-alert-card">
          <div className="scheduler-section-title">当前执行结果</div>
          <div className="scheduler-alert-group">
            {recentResults.length ? recentResults.map((entry, index) => (
              <div className="scheduler-result-item" key={`${entry.task_name}-execution-${index}`}>
                <strong>{formatReadableTaskTitle(entry.task_name)}</strong>
                <p>{formatReadableDetail(entry.result.content, '暂无结果摘要')}</p>
                <small>{formatReadableTime(entry.updated_at)}</small>
              </div>
            )) : <div className="auto-task-empty">暂无结果</div>}
          </div>
        </section>
      </div>

      <div className="scheduler-summary-grid scheduler-centers-grid">
        <section className="scheduler-alert-card">
          <div className="scheduler-section-title">发布中心</div>
          <div className="scheduler-alert-group">
            {publishSourceGroups.length ? publishSourceGroups.map(([sourceKey, entries], groupIndex) => (
              <section className="scheduler-archive-group" key={`${sourceKey}-${groupIndex}`}>
                <div className="scheduler-archive-group-head">
                  <strong>{formatReadableDetail(sourceKey)}</strong>
                  <span>{entries.length} 个版本</span>
                </div>
                <div className="scheduler-alert-group">
                  {entries.map((entry, index) => (
                    <PublishCenterCard
                      key={`${entry.taskName}-publish-${index}`}
                      entry={entry}
                      templatePool={templatePool}
                      controlLoadingTask={controlLoadingTask}
                      onManualPublished={(taskName) => onManualControlTask(taskName, 'mark_manual_published')}
                    />
                  ))}
                </div>
              </section>
            )) : <div className="auto-task-empty">暂无可发布结果</div>}
          </div>
        </section>

        <section className="scheduler-alert-card">
          <div className="scheduler-section-title">结果沉淀中心</div>
          <div className="scheduler-task-groups">
            {archiveContentLineGroups.map(([line, entries], index) => <div className="scheduler-task-group-chip" key={`content-${line}-${index}`}><strong>内容线</strong><span>{formatContentLine(line)} · {entries.length} 条</span></div>)}
            {archiveAccountLineGroups.map(([line, entries], index) => <div className="scheduler-task-group-chip" key={`account-${line}-${index}`}><strong>账号</strong><span>{entries[0]?.accountDisplay || formatAccountLine(line)} · {entries.length} 条</span></div>)}
          </div>
          <div className="scheduler-task-groups">
            {archiveStructureGroups.map((entry, index) => (
              <div className="scheduler-task-group-chip" key={`structure-${entry.structureId}-${index}`}>
                <strong>结构模板</strong>
                <span>{formatReadableDetail(entry.structureId)} · 复用 {entry.count} 次 · 主账号 {formatAccountLine(entry.topAccount?.[0])} ({entry.topAccount?.[1] || 0})</span>
              </div>
            ))}
          </div>
          <div className="scheduler-archive-groups">
            {archiveDomainGroups.length ? archiveDomainGroups.map(([domain, entries], groupIndex) => (
              <section className="scheduler-archive-group" key={`${domain}-${groupIndex}`}>
                <div className="scheduler-archive-group-head">
                  <strong>{formatReadableDetail(domain)}</strong>
                  <span>{entries.length} 条</span>
                </div>
                <div className="scheduler-alert-group">
                  {entries.map((entry, index) => (
                    <ArchiveCenterCard
                      key={`${entry.taskName}-archive-${index}`}
                      entry={entry}
                      templatePool={templatePool}
                      controlLoadingTask={controlLoadingTask}
                      onMarkTemplateSource={onMarkTemplateSource}
                    />
                  ))}
                </div>
              </section>
            )) : <div className="auto-task-empty">暂无可沉淀结果</div>}
          </div>
        </section>
      </div>
    </>
  )
}
